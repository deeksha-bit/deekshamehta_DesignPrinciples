package Operations;

public class ArithmeticOperation {

    public float add(int a,int b)
    {
        return a+b;
    }
    public float subtract(int a,int b)
    {
        return a-b;
    }
    public float divide(int a,int b)
    {
        return (float)a/b;
    }
    public float multiply(int a,int b)
    {
        return (float)a*b;
    }
}
